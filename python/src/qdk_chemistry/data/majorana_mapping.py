"""Majorana-to-Pauli mapping data class for fermion-to-qubit encodings.

This module provides the :class:`MajoranaMapping` data class, which describes
a fermion-to-qubit encoding. The unified primitive across all f2q encodings
is the **bilinear** ``i·gamma_j·gamma_k`` (see :py:meth:`MajoranaMapping.bilinear`):
the parity-even subalgebra of the Majorana Clifford algebra is generated by
these bilinears in every encoding, including redundant encodings (e.g.
Bravyi-Kitaev superfast, Verstraete-Cirac, Derby-Klassen) where individual
Majoranas have no Pauli image.

Standard **Majorana-atomic** encodings (Jordan-Wigner, Bravyi-Kitaev tree,
parity, and their tapered variants) additionally provide a Pauli image for
each individual Majorana operator gamma_k via the ``table`` and
:py:meth:`MajoranaMapping.majorana` interfaces. The factory class methods
on :class:`MajoranaMapping` produce these Majorana-atomic encodings; custom
Majorana-atomic encodings can be constructed from a Pauli table.

References:
    * Chen, Xu, Boettcher, "Equivalence between fermion-to-qubit mappings in
      two spatial dimensions" (2023) — every f2q mapping as a homomorphism
      from the parity-even Majorana Clifford algebra into the Pauli group,
      modulo stabilizers.
    * Jiang, Kalev, Mruczkiewicz, Neven, "Optimal fermion-to-qubit mapping
      via ternary trees..." (2020), and the Majorana loop stabilizer code
      literature.

"""

