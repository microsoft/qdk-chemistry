/**
 * @brief Helper function to calculate isotope enum value
 * @param Z Atomic number (proton count)
 * @param A Mass number (proton + neutron count)
 * @return Encoded unsigned value for isotope representation
 *
 * For isotopes, put the atomic mass number in the enum's bit representation.
 * Z ranges up to 118 < 128 = 2^7, so we use 7 bits for Z.
 * A ranges up to 295 < 512 = 2^9, so we need at least 16 bits total.
 */
constexpr unsigned isotope(const unsigned Z, const unsigned A) noexcept {
  // Z ranges up to 118 < 128 = 2^7
  constexpr unsigned Z_bits = 7;
  // A ranges up to 295 < 512 = 2^9
  // so we need at least 16 bits to represent both in the underlying value
  static_assert(sizeof(unsigned) >= 2,
                "Unsigned type is smaller than two bytes, isotope element "
                "types do not work");

  return (A << Z_bits) + Z;
}

